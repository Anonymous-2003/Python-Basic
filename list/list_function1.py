l = [10,20,30,40,50,20]
c = l.count(20)   # returns how many times 20 is repeated in list
print(c)
m = max(l)  #  returns the maximum value in list
print(m)
mn = min(l) #   returns the minimum value in list
print(mn)
l.reverse() #reverses the elements on list
print(l)
l.sort()    
print(l)
i = l.index(20) #returns on what index given value is stored
print(i)
